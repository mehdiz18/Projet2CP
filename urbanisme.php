        <h3>Permis de construire</h3>
        <details>
          <summary>Dossier à fournir</summary>
          <ol type="I">
            <li>
              <h5>Dossier administratif</h5>
              <ul>
                <li>les références du permis de lotir pour les constructions projetées sur un terrain faisant partie d'un lotissement à usage d'habitation ou autre,</li>
                <li>l'arrêté de l’autorité compétente autorisant la création ou l'extension d’établissements industriels et commerciaux classés dans les catégories d'établissements dangereux, insalubres et incommodes ;</li>
                <li>le certificat de viabilité délivré conformément aux dispositions citées ci-dessus, pour les constructions situées dans un lotissement autorisé par un permis de lotir.</li>
              </ul>
            </li>
            <li>
              <h5>Dossier architectural</h5>
              <ul>
                <li>le plan de situation à une échelle appropriée permettant la localisation du projet ;</li>
                <li>
                  <p>le plan de masse à l’échelle 1/200ème pour les
                    parcelles de moins ou égale à 500 m 2 ou 1/500ème pour
                    les parcelles de moins ou égale à 5000 m 2 et plus de 500
                    m 2 et 1/1000ème pour les parcelles de plus de 5000 m 2
                    comportant les indications suivantes :</p>
                    <ul>
                      <li>les limites du terrain, sa superficie, son orientation, et le tracé des clôtures, le cas échéant ;</li>
                      <li>les courbes de niveau ou la surface de nivellement, et les coupes schématiques du terrain ;</li>
                      <li>la nature, la hauteur ou le nombre d'étages des constructions voisines ;</li>
                      <li>la hauteur ou le nombre d'étages des constructions existantes et projetées sur le terrain, ainsi que la destination des espaces construits et non construits ;</li>
                      <li>la surface totale de planchers et la surface construite au sol ;</li>
                      <li>l'indication des réseaux de viabilité desservant le terrain avec leurs caractéristiques techniques principales ainsi que, les points de raccordement et le tracé des voiries et réseaux projetés sur le terrain ;</li>
                    </ul>
                </li>
                <li>les différents plans, à l'échelle 1/50ème pour les constructions dont l’emprise au sol est moins de 300m 2 , à l’échelle 1/100ème pour les constructions dont l’emprise au sol est entre 300m 2 et 600m 2 et à l’échelle 1/200ème pour le reste des constructions : des distributions intérieurs des différents niveaux de construction, les locaux techniques,ainsi que les façades y compris celles des clôtures, les coupes utiles et les images en trois dimensions permettant de situer le projet dans son environnement proche, le cas échéant.
                La destination des différents locaux devra figurer sur les plans.
                Pour les projets de transformation de façades ou de gros œuvres, les parties anciennes conservées ou démolies ainsi que celles projetées, doivent apparaître distinctement.
                </li>
                <li>une notice comporte le devis descriptif et estimatif des travaux ainsi que les délais de réalisation.</li>
                <li>les pièces écrites et graphiques indiquant la construction par tranche, s’il y a lieu.</li>
              </ul>
            </li>
            <li>
              <h5>Dossier technique</h5>
                <ol>
                  <li>
                    Hormis les projets de construction des habitations individuelles, il est exigé une notice accompagnée de schémas utiles et comportant les indications suivantes :
                    <ul>
                      <li>les effectifs et la capacité d'accueil de chaque local ;</li>
                      <li>le mode de construction de toitures, ainsi que la nature des matériaux utilisés ;</li>
                      <li>la description sommaire des dispositifs d'alimentation en électricité, de gaz, de chauffage, d'adduction en eau potable, d'assainissement et d'aération ;</li>
                      <li>les moyens particuliers de défense et de secours contre l'incendie ;</li>
                      <li>la nature et les quantités de substances liquides, solides ou gazeuses préjudiciables à la santé publique, à l'agriculture et à l'environnement, contenues dans les eaux usées évacuées et les émissions gazeuses, ainsi que le  dispositif de traitement, de stockage et de filtrage ;</li>
                      <li>les plans des réseaux d'évacuation des eaux usées ;</li>
                      <li>la description sommaire des organes de production, de transformation, de stockage des matières premières et des produits manufacturés pour les bâtiments industriels ;</li>
                      <li>le niveau du bruit produit, pour les constructions à usage industriel et commercial et établissements destinés à recevoir du public.</li>
                    </ul>
                  </li>
                  <li>
                    <p>L’étude de génie civil comprenant :</p>
                    <ul style="list-style-type: circle;">
                      <li>
                        un rapport, établi et signé par un ingénieur agréé en génie civil, précisant :
                        <ul>
                          <li>la définition et la description de la structure porteuse ;</li>
                          <li>le dimensionnement des ouvrages et des éléments composant la structure ;</li>
                        </ul>
                      </li>
                      <li>les vues en plans des structures aux mêmes échelles que ceux des plans du dossier d’architecture.</li>
                    </ul>
                  </li>
                </ol>
            </li>
          </ol>
          <p>La demande de permis de construire et les dossiers qui l'accompagnent sont adressés au président de l'assemblée populaire communale de la commune du lieu d'implantation en trois (3) exemplaires pour les projets destinés à l’habitation individuelle et huit (8) exemplaires pour le reste des projets qui nécessitent la consultation des services publics.</p>
        </details>
