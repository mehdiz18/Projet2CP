        <h5>Permis de demolir</h5>
        <details>
          <summary>Dossier à fournir</summary>
          <ul>
            <li>une copie de l'acte de propriété, ou du certificat possession, conformément aux dispositions de la loi n° 90-25 du 18 novembre 1990, susvisée ;</li>
            <li>un mandat conformément aux dispositions de l'ordonnance n° 75-58 du 26 septembre 1975, susvisée ;</li>
            <li>une ampliation de l'acte administratif, ayant prononcé l'affectation de l'immeuble considéré ;</li>
            <li>une copie du statut lorsque le propriétaire ou le mandataire est une personne morale.</li>
            <li>un plan de situation à une échelle appropriée permettant la localisation du projet ;</li>
            <li>un plan de masse à l'échelle 1/500 ou 1/200 de la construction à démolir ;</li>
            <li>un rapport et engagement sur le phasage de l’opération de démolition et les moyens à utiliser pour ne pas perturber la stabilité de la zone ;</li>
            <li>lorsque la démolition de l’immeuble est à moins de trois (3) mètres des immeubles bâtis avoisinants, un rapport d’expertise visé par un ingénieur en génie civil est obligatoire faisant mention de la méthode à utiliser dans l’opération de démolition mécanique ou manuelle, le matériel utilisé et moyens à déployer pour stabiliser les immeubles mitoyens ;</li>
            <li>le planning et le délai d’exécution ;</li>
            <li>un plan au 1/100 de la construction précisant la partie à démolir et la partie à conserver dans le cas de démolition partielle ;</li>
            <li>l'affectation éventuelle du site libéré.</li>
          </ul>
          <p>La demande de permis de démolir et les dossiers qui l'accompagnent sont adressés en trois (3) exemplaires au président de l'assemblée populaire communale du lieu d'implantation de la construction.</p>
