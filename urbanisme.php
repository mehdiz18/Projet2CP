        <h3>Permis de lotir</h3>
        <details>
          <summary>Dossier à fournir</summary>
          <p>La demande de permis de lotir dont le modèle-type est joint en annexe du présent décret, doit être formulée et signée par le propriétaire ou son mandataire.
          L'intéressé doit fournir à l'appui de sa demande :</p>
          <ul>
            <li>soit une copie de l'acte de propriété ;</li>
            <li>lsoit un mandat conformément aux dispositions de l'ordonnance n°75-58 du 26 septembre 1975, susvisée ;</li>
            <li>soit une copie du statut lorsque le propriétaire ou le mandataire est une personne morale.</li>
            <li>le plan de situation établi à une échelle appropriée permettant la localisation du terrain ;</li>
            <li>
              les plans utiles à l'échelle 1/200ème ou 1/500ème comportant les indications suivantes :
              <ul>
                <li>les limites de terrain et sa superficie,</li>
                <li>les courbes de niveau et la surface de nivellement,avec leurs caractéristiques techniques principales et les points de raccordement des voies et réseaux divers ;</li>
                <li>la délimitation des lots projetés avec le tracé de la voirie, des réseaux d'alimentation en eau potable, d'incendie, d'évacuation des eaux usées ainsi que des réseaux de distribution de gaz, d'électricité, detéléphone et d'éclairage public ;</li>
                <li>la localisation des aires de stationnement et des espaces libres et celle des servitudes particulières ;</li>
                <li>l'implantation, la nature et la forme urbaine des constructions projetées y compris celles relatives aux équipements collectifs ;</li>
              </ul>
              </li>
            <li>
              <p>une notice précisant les dispositifs relatifs :</p>
              <ul>
                  <li>au traitement destiné à débarrasser les eaux résiduaires industrielles de toute substance liquide, solide ou gazeuse préjudiciable à la santé publique, à l'agriculture ou à l'environnement ;</li>
                  <li>au traitement destiné à débarrasser les fumées et émissions gazeuses de toute substance préjudiciable à la santé publique ;</li>
                  <li>à la limitation du niveau de bruit et d'émission de parasites électromagnétiques, pour les lotissements à usage industriel ;</li>
              </ul>
            </li>
            <li>
              <p>une notice comportant les indications suivantes :</p>
              <ul>
                <li>la liste des lots et leur superficie respective ;</li>
                <li>la nature des diverses occupations précisant le nombre de logements, d'emplois et la population totale résidente ;</li>
                <li>les besoins induits en eau, gaz, électricité, transport et les modalités de leur satisfaction ;</li>
                <li>la nature des servitudes et nuisances éventuelles ;</li>
                <li>le cas échéant, l'étude d'impact sur l'environnement ;</li>
                <li>une étude géotechnique élaborée par un laboratoire agréé.</li>
              </ul>
            </li>
            <li>le programme des travaux indiquant les caractéristiques techniques des ouvrages, réseaux et aménagements à réaliser et les conditions de leur mise en œuvre, avec une estimation de leur coût et précisant, le cas échéant, les tranches de réalisation et leurs délais ;</li>
            <li>un cahier des charges fixant les obligations et servitudes fonctionnelles et d'intérêt général, imposées dans le lotissement ainsi que les conditions dans lesquelles les constructions doivent être édifiées.</li>
            </ul>
            <p>Dans tous les cas, la demande de permis de lotir et les pièces qui l'accompagnent, sont adressées en cinq (5) exemplaires au président de l'assemblée populaire communale de lieu d'implantation.</p>
